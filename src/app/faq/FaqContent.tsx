function FaqItem({ q, a }: { q: string; a: string }) {
  const [open, setOpen] = useState(false)
  return (
    <div style={{ borderBottom: '1px solid var(--border)' }}>
      <button
        onClick={() => setOpen(!open)}
        style={{
          width: '100%',
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'space-between',
          padding: '1.25rem 0',
          background: 'none',
          border: 'none',
          cursor: 'pointer',
          textAlign: 'left',
          gap: '1rem',
        }}
      >
        <span style={{ fontWeight: 600, color: open ? 'var(--primary)' : 'var(--text)', fontSize: '0.975rem', lineHeight: 1.4, transition: 'color 0.2s' }}>
          {q}
        </span>
        <span style={{
          width: '1.75rem', height: '1.75rem', borderRadius: '50%',
          background: open ? 'var(--primary)' : 'var(--primary-light)',
          display: 'flex', alignItems: 'center', justifyContent: 'center',
          flexShrink: 0, transition: 'background 0.2s',
        }}>
          {open
            ? <Minus size={13} color="white" />
            : <Plus size={13} color="var(--primary)" />
          }
        </span>
      </button>
      <div style={{
        overflow: 'hidden',
        maxHeight: open ? '600px' : '0',
        transition: 'max-height 0.35s ease',
      }}>
        <p style={{ paddingBottom: '1.5rem', color: 'var(--text-muted)', lineHeight: 1.8, fontSize: '0.925rem' }}>
          {a}
        </p>
      </div>
    </div>
  )
}

